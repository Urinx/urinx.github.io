$(document).ready(function(){
	// 锚点平滑跳转
	$('nav li a').click(function(){
		$("html,body").animate({scrollTop:$("#"+this.text.toLowerCase()).offset().top},2000);
	});
	$('a.down-icon').click(function(){
		$("html,body").animate({scrollTop:$("#terminal").offset().top},500);
	});

	// Set work content
	var section = '';
	for (var i = 0; i < works_row; i++) {
		var row = '<div class="row">';
		for (var j = 0; j < works_col; j++) {
			var work = works[i * works_col + j];
			if (work) {
			var temp = work_template;
				for (key in work) {
					temp = temp.replace(eval('/###_'+key.toUpperCase()+'_###/g'),work[key]);
				}
				if (work['website']) {
					temp = temp.replace('###_IF1_###','');
					temp = temp.replace('###_FI1_###','');
				} else {
					temp = temp.replace('###_IF1_###','<!-- ');
					temp = temp.replace('###_FI1_###',' -->');
				}
				if (work['github']) {
					temp = temp.replace('###_IF2_###','');
					temp = temp.replace('###_FI2_###','');
				} else {
					temp = temp.replace('###_IF2_###','<!-- ');
					temp = temp.replace('###_FI2_###',' -->');
				}
				row += temp;
			}
		}
		row += '</div>';
		section += row;
	}
	var html = '<div class="container"><div class="section">'+section+'</div></div>';
	$('works').html(html);

	// Set article content
	var section = '';
	for (var i = 0; i < articles_row; i++) {
		var row = '<div class="row">';
		for (var j = 0; j < articles_col; j++) {
			var article = articles[i * articles_col + j];
			if (article) {
				var temp = article_template;
				for (key in article) {
					temp = temp.replace(eval('/###_'+key.toUpperCase()+'_###/g'),article[key]);
				}
				row += temp;
			}
		}
		row += '</div>';
		section += row;
	}
	var html = '<div class="container"><div class="section">'+section+'</div></div>';
	$('articles').html(html);
});






